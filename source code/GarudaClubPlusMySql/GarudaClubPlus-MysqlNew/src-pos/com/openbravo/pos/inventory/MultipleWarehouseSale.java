    private void save() throws BasicException {
        if (jTextField1.getText().length() > 0) {
            if (wmodel.getSize() > 0) {
               
                    String location = null;
                    LocationInfo l  = (LocationInfo) parent.getSelectedItem();
                    for (int i = 0; i < wmodel.getSize(); i++) {
                        if (location == null) {
                            location = wareho.get(wmodel.getElementAt(i).toString());
                        } else {
                            location = location + "#" + wareho.get(wmodel.getElementAt(i).toString());
                        }
                    }
                    Object[] obj = new Object[]{UUID.randomUUID().toString(), jTextField1.getText(),l.getID(), location, reportwise, reporttype, true};
                    new StaticSentence(m_App.getSession(), "INSERT INTO CONSOLIDATEREPORT(ID,NAME,PARENTWARE,WAREHOUSE,REPORTWISE,REPORTTYPE,FLAG) VALUES(?,?,?,?,?,?,?)", new SerializerWriteBasic(new Datas[]{Datas.STRING, Datas.STRING, Datas.STRING, Datas.STRING, Datas.STRING, Datas.STRING, Datas.BOOLEAN}), null).exec(obj);
               

            } else {
                JOptionPane.showMessageDialog(this, "select warehouse", "incomplete form", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter name", "incomplete form", JOptionPane.WARNING_MESSAGE);
        }
        load();
        jComboBox1.setSelectedIndex(-1);
    }

