    private void insertlib_media() {
        
         try{
          if(nme_txt.getText().length()>0){
              
             Object[] param=new Object[]{UUID.randomUUID().toString(),nme_txt.getText(),m_App.getAppUserView().getUser().getName(),new Date(),m_App.getProperties().getHost(),true};
             new PreparedSentence(m_App.getSession()
                , "INSERT INTO lib_media (ID,NAME,CREATEDBY,CREATEDATE,CREATEDHOST,ACTIVE) VALUES (?,?,?,?,?,?)"
                , new SerializerWriteBasic(new Datas[]{Datas.STRING,Datas.STRING,Datas.STRING,Datas.TIMESTAMP,Datas.STRING,Datas.BOOLEAN})).exec(param);
          JOptionPane.showMessageDialog(this, "Created Successfully..!", "Success", JOptionPane.INFORMATION_MESSAGE);
           }

          
          reset();  
              
          
         }catch(NullPointerException e){
          
        e.printStackTrace();
      }
      catch(Exception e){
       e.printStackTrace();
      }
    }
    
    private void Update_media() {
           
           try{
          if(nme_txt.getText().length()>0){
           
             Object[] param=new Object[]{nme_txt.getText(),m_App.getAppUserView().getUser().getName(),new Date(),m_App.getProperties().getHost(),true,m_id};
             new PreparedSentence(m_App.getSession()
                , "UPDATE lib_media SET NAME=?,CREATEDBY=?,CREATEDATE=?,CREATEDHOST=?,ACTIVE=? WHERE ID=?"
                , new SerializerWriteBasic(new Datas[]{Datas.STRING,Datas.STRING,Datas.TIMESTAMP,Datas.STRING,Datas.BOOLEAN,Datas.STRING})).exec(param);
             JOptionPane.showMessageDialog(this, "Updated Successfully..!", "Success", JOptionPane.INFORMATION_MESSAGE);
           }

           imodel  = Lib_MediaTableModel.loadInstance(m_App,1);
           jTable1.setModel(imodel.getTableModel()); 
          reset(); 
              
         }catch(NullPointerException e){
          
        e.printStackTrace();
      }
      catch(Exception e){
       e.printStackTrace();
      }
       }
    
    private void deact_media() {
           try{
           String newbookId = UUID.randomUUID().toString();
           
             new PreparedSentence(m_App.getSession(), "UPDATE lib_media  SET ID=?, ACTIVE=0  , DEACTBY=? , DEACTDATE=? , DEACTHOST=?,DEACTREFERENCE=? WHERE ID = ? and DEACTBY is null and DEACTDATE is null"
                                                                           , new SerializerWriteBasic(new Datas[]{ Datas.STRING ,Datas.STRING , Datas.TIMESTAMP , Datas.STRING ,Datas.STRING , Datas.STRING })).exec
                                                                            (new Object[]{ newbookId, m_App.getAppUserView().getUser().getName() ,new Date(), m_App.getProperties().getHost(),deact_mid , deact_mid  });
       imodel  = Lib_MediaTableModel.loadInstance(m_App,1);
       jTable1.setModel(imodel.getTableModel()); 
       
       reset();
       JOptionPane.showMessageDialog(this, "De-Activated Successfully..!", "Success", JOptionPane.INFORMATION_MESSAGE);
           }catch(NullPointerException e){
          
        e.printStackTrace();
      }
      catch(Exception e){
       e.printStackTrace();
      }
     }

