    private void insertlib_barcode(String flag) {
        
         try{
          //if(name_txt.getText().length()>0){
              
             Object[] param=new Object[]{UUID.randomUUID().toString(),jLabel1.getText(),flag};
             new PreparedSentence(m_App.getSession()
                , "INSERT INTO generaltable (ID,NAME,VALUE) VALUES (?,?,?)"
                , new SerializerWriteBasic(new Datas[]{Datas.STRING,Datas.STRING,Datas.STRING})).exec(param);
          //JOptionPane.showMessageDialog(this, "Created Successfully..!", "Success", JOptionPane.INFORMATION_MESSAGE);
           
         }catch(NullPointerException e){
          
        e.printStackTrace();
      }
      catch(Exception e){
       e.printStackTrace();
      }
    }

    private void Update_barcode(String flag) {
           
           try{
          
           
             Object[] param=new Object[]{flag,jLabel1.getText()};
             new PreparedSentence(m_App.getSession()
                , "UPDATE generaltable SET VALUE=? WHERE NAME=?"
                , new SerializerWriteBasic(new Datas[]{Datas.STRING,Datas.STRING})).exec(param);
             JOptionPane.showMessageDialog(this, "Updated Successfully..!", "Success", JOptionPane.INFORMATION_MESSAGE);
           
              
         }catch(NullPointerException e){
          
        e.printStackTrace();
      }
      catch(Exception e){
       e.printStackTrace();
      }
       }
