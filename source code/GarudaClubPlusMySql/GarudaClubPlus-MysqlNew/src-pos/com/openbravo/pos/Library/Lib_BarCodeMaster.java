    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<Object> obj=new ArrayList<Object>();
        try {
            obj=(List<Object>) new StaticSentence(m_App.getSession(), "SELECT Name FROM generaltable",  SerializerWriteString.INSTANCE , SerializerReadString.INSTANCE).list();
            
        } catch (BasicException ex) {
            Logger.getLogger(Lib_BarCodeMaster.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(!obj.contains(jLabel1.getText())){
        if(jRadioButton1.isSelected()){
            insertlib_barcode("1");
        }else if(jRadioButton2.isSelected()){
            insertlib_barcode("0");
        }
        }else{
            if(jRadioButton1.isSelected()){
            Update_barcode("1");
        }else if(jRadioButton2.isSelected()){
            Update_barcode("0");
        }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    

