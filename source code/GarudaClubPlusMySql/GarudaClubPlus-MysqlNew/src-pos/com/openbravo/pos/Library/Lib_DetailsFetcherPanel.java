    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
       FileNameExtensionFilter filter = new FileNameExtensionFilter("Exel Files", "xls", "xlsx", "xlsm","ods");
       filechooser1.setName("filechooser");
       filechooser1.setDialogTitle("Choose the FILE to Upload");
       filechooser1.setDragEnabled(true);
       filechooser1.setFileFilter(filter);
       int returnVal = filechooser1.showOpenDialog(null);
       if (returnVal == JFileChooser.APPROVE_OPTION) {
       fname = filechooser1.getSelectedFile().toString();
       file_txt.setText(fname);
       }else {
        JOptionPane.showMessageDialog(this, "No Selection", "Error", JOptionPane.OK_OPTION);
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

