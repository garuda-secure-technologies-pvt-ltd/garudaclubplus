package com.openbravo.pos.pda.struts.forms;

import org.apache.struts.validator.ValidatorForm;

public class DeactivateFacilityForm extends ValidatorForm {

    private String date;
    private String[] mfuid;
    private String id;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String[] getMfuid() {
        return mfuid;
    }

    public void setMfuid(String[] mfuid) {
        this.mfuid = mfuid;
    }
    

    

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
   

    


